const uint8_t SegmentMultiplex[4] = {
  0x70, 0xB0, 0xD0, 0xE0
};

/* Private variables ---------------------------------------------------------*/
volatile uint8_t LEDs = 0x0F;
volatile uint8_t Segments[4] = { 0, 1, 2, 3 };

/* Private functions ---------------------------------------------------------*/
void ShiftOut(uint32_t data)
{
	ClrLAT;
	for (int i = 0; i < 16; i++)
  {
		ClrCLK;
		for (volatile int j = 0; j < 16; j++) __NOP();
		if (data & 0x8000) SetSDI; else ClrSDI;
		data <<= 1;
		SetCLK;
	}
	SetLAT;
	for (volatile int j = 0; j < 16; j++) __NOP();
	ClrCLK;
	ClrLAT;
}

/* Exported functions ------------------------------------------------------- */
void DisplayRun(void)
{
  static unsigned int index = 0;
  index = index == 3 ? 0 : index + 1;
  uint32_t data = Segments[index] << 8 | SegmentMultiplex[index] | LEDs;
  ShiftOut(data);
}

void DisplayString(char* text)
{
	*((uint32_t *)Segments) = 0;
	for (int i = 0; i < 4; i++)
	{
		if (*text == '\0')
			break;
		if (*text == '.')
		{
			Segments[i - 1] |= AsciiTable['.'];
			text++;
		}
		Segments[i] |= AsciiTable[*text];
		text++;
	}
}

void DisplayLed(int led, int state)
{
	if (0 <= led && led <= 3)
	{
		if (state)
			LEDs |= 1 << led;
		else
			LEDs &= 0x0F & ~(1 << led);
	}
}
